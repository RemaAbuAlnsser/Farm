export default function Guide() {
  const [search, setSearch] = useState("");
  const q = search.trim().toLowerCase();

  const filtered = q
    ? SECTIONS.filter(
        (s) =>
          s.title.toLowerCase().includes(q) ||
          s.content.some((c) => c.toLowerCase().includes(q))
      )
    : SECTIONS;

  const highlight = (text) => {
    if (!q) return text;
    const idx = text.toLowerCase().indexOf(q);
    if (idx === -1) return text;
    return (
      <>
        {text.slice(0, idx)}
        <mark style={{ background: "#fef08a", borderRadius: 3, padding: "0 2px" }}>
          {text.slice(idx, idx + q.length)}
        </mark>
        {text.slice(idx + q.length)}
      </>
    );
  };

  return (
    <div>
      <div className="page-header">
        <h1>دليل الاستخدام</h1>
      </div>

      <div style={{ marginBottom: 24 }}>
        <input
          type="text"
          placeholder="ابحث في الدليل..."
          value={search}
          onChange={(e) => setSearch(e.target.value)}
          style={{
            width: "100%", maxWidth: 380, padding: "10px 16px",
            border: "1px solid #e2e8f0", borderRadius: 8,
            fontSize: "0.95rem", fontFamily: "inherit", outline: "none",
            boxSizing: "border-box",
          }}
        />
      </div>

      {filtered.length === 0 ? (
        <p style={{ color: "#94a3b8", padding: "20px 0" }}>لا توجد نتائج للبحث</p>
      ) : (
        <div style={{ display: "flex", flexDirection: "column", gap: 16, maxWidth: 720 }}>
          {filtered.map((sec) => (
            <div key={sec.title} style={{
              background: "#fff",
              borderRadius: 12,
              border: "1px solid #e2e8f0",
              overflow: "hidden",
            }}>
              <div style={{
                padding: "12px 20px",
                background: "#f8fafc",
                borderBottom: "1px solid #e2e8f0",
                fontWeight: 700,
                fontSize: "0.95rem",
                color: "#0f172a",
              }}>
                {highlight(sec.title)}
              </div>
              <ul style={{ margin: 0, padding: "12px 20px 12px 36px", display: "flex", flexDirection: "column", gap: 8 }}>
                {sec.content.map((line, i) => (
                  <li key={i} style={{ fontSize: "0.88rem", color: "#334155", lineHeight: 1.7 }}>
                    {highlight(line)}
                  </li>
                ))}
              </ul>
            </div>
          ))}
        </div>
      )}
    </div>
  );
}
